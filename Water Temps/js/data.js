function filterReadings( readings, filters ) {
    return readings.filter( ( r ) => {
        if ( filters.search ) {
            const q = filters.search.toLowerCase();
            if ( !( r.waterBody || '' ).toLowerCase().includes( q ) ) return false;
        }

        if ( filters.types && filters.types.length > 0 ) {
            if ( !filters.types.includes( r.type ) ) return false;
        }

        if ( filters.source && filters.source !== 'all' ) {
            if ( r.source !== filters.source ) return false;
        }

        if ( filters.dateFrom ) {
            if ( new Date( r.date ) < new Date( filters.dateFrom ) ) return false;
        }

        if ( filters.dateTo ) {
            const to = new Date( filters.dateTo );
            to.setHours( 23, 59, 59, 999 );
            if ( new Date( r.date ) > to ) return false;
        }

        if ( filters.tempMin !== undefined && r.tempC < filters.tempMin ) return false;
        if ( filters.tempMax !== undefined && r.tempC > filters.tempMax ) return false;

        if ( filters.nearLat !== undefined && filters.nearLng !== undefined && filters.nearRadius ) {
            if ( haversineKm( r.lat, r.lng, filters.nearLat, filters.nearLng ) > filters.nearRadius ) return false;
        }

        return true;
    });
}

function haversineKm( lat1, lng1, lat2, lng2 ) {
    const R = 6371;
    const dLat = ( lat2 - lat1 ) * Math.PI / 180;
    const dLng = ( lng2 - lng1 ) * Math.PI / 180;
    const a = Math.sin( dLat / 2 ) ** 2 +
        Math.cos( lat1 * Math.PI / 180 ) * Math.cos( lat2 * Math.PI / 180 ) *
        Math.sin( dLng / 2 ) ** 2;
    return R * 2 * Math.atan2( Math.sqrt( a ), Math.sqrt( 1 - a ) );
}

function maskEmail( email ) {
    const at = email.indexOf( '@' );
    if ( at < 2 ) return email;
    return email.slice( 0, 2 ) + '***' + email.slice( at );
}

function cToF( c ) {
    return Math.round( c * 9 / 5 + 32 );
}

function fToC( f ) {
    return ( f - 32 ) * 5 / 9;
}

function capitalize( str ) {
    return str.charAt( 0 ).toUpperCase() + str.slice( 1 );
}

function escapeHtml( str ) {
    const map = { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' };
    return str.replace( /[&<>"']/g, ( c ) => map[c] );
}

function formatDateTime( iso ) {
    if ( !iso ) return '';
    if ( iso.length <= 10 ) {
        const [y, m, d] = iso.split( '-' ).map( Number );
        return new Date( y, m - 1, d ).toLocaleDateString( 'en-US', {
            year: 'numeric', month: 'short', day: 'numeric'
        });
    }
    return new Date( iso ).toLocaleString( 'en-US', {
        year: 'numeric', month: 'short', day: 'numeric',
        hour: '2-digit', minute: '2-digit'
    });
}
